(function () {
  var LANGS = ['fr','en','es','ja'];
  var LABELS = { fr:'FR', en:'EN', es:'ES', ja:'日' };
  var currentLang = localStorage.getItem('portfolio-lang') || 'fr';

  function t(key) {
    return (TRANSLATIONS[currentLang]||{})[key] || (TRANSLATIONS.fr[key]) || key;
  }

  function apply() {
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
      el.innerHTML = t(el.getAttribute('data-i18n'));
    });
    document.documentElement.lang = currentLang;
    document.querySelectorAll('.lang-btn').forEach(function(b) {
      b.classList.toggle('active', b.dataset.lang === currentLang);
    });
  }

  function buildSwitcher() {
    var nav = document.querySelector('nav');
    if (!nav) return;
    var sw = document.createElement('div');
    sw.className = 'lang-switcher';
    LANGS.forEach(function(lang) {
      var btn = document.createElement('button');
      btn.className = 'lang-btn';
      btn.dataset.lang = lang;
      btn.textContent = LABELS[lang];
      btn.addEventListener('click', function() {
        currentLang = lang;
        localStorage.setItem('portfolio-lang', lang);
        apply();
      });
      sw.appendChild(btn);
    });
    var hamburger = nav.querySelector('.nav-hamburger');
    hamburger ? nav.insertBefore(sw, hamburger) : nav.appendChild(sw);
  }

  document.addEventListener('DOMContentLoaded', function() {
    buildSwitcher();
    apply();
  });
})();
